# Create a tuple
my_tuple = ("Apple", "Banana", "Cherry")

print("Original Tuple:", my_tuple)

# Access elements
print("First element:", my_tuple[0])
print("Last element:", my_tuple[-1])

# Tuple concatenation
tuple1 = ("Date", "Elderberry")
tuple2 = ("Fig", "Grape")
print("Concatenated Tuple:", tuple1 + tuple2)

# Tuple length
print("Tuple length:", len(my_tuple))

# Tuple conversion to list
print("Tuple converted to list:", list(my_tuple))

# Tuple conversion to string
print("Tuple converted to string:", ", ".join(my_tuple))